/**
 * @description 尝试创建 OpenCC 转换器；若运行环境中未安装或不可用则返回 null。
 * @returns {(null|((text:string) => string))} OpenCC 转换函数。
 */
function createOpenCcConverter() {
  try {
    // opencc-js 的不同版本导出形式略有差异，这里统一做兼容尝试。
    const opencc = require("opencc-js");
    if (typeof opencc === "function") {
      return opencc;
    }
    if (opencc && typeof opencc.Converter === "function") {
      return opencc.Converter({ from: "t", to: "cn" });
    }
    if (opencc && typeof opencc.OpenCC === "function") {
      const converter = new opencc.OpenCC("t2s.json");
      if (converter && typeof converter.convertSync === "function") {
        return (text) => converter.convertSync(text);
      }
    }
  } catch (error) {
    return null;
  }
  return null;
}

const openCcConverter = createOpenCcConverter();

/**
 * @description 使用内置常见繁简字符映射进行逐字转换，作为 OpenCC 缺失时的兜底方案。
 * @param {string} text 原始文本。
 * @returns {string} 转换后的简体文本。
 */
function convertWithFallbackMap(text) {
  return Array.from(String(text || ""))
    .map((char) => FALLBACK_TRADITIONAL_TO_SIMPLIFIED.get(char) || char)
    .join("");
}

/**
 * @description 将原文转换为简体字；优先使用 OpenCC，失败时回退到内置映射。
 * @param {string} text 原始文本。
 * @returns {string} 简体转写文本。
 */
function convertToSimplified(text) {
  const value = String(text || "");
  if (!value) {
    return "";
  }
  if (openCcConverter) {
    try {
      return String(openCcConverter(value));
    } catch (error) {
      return convertWithFallbackMap(value);
    }
  }
  return convertWithFallbackMap(value);
}

